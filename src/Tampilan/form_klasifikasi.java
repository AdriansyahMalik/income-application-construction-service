    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(kode_klasifikasi.getText().equals("") ||subkualifikasi.getText().equals("") || kode_subklasifikasi.getText().equals("")|| subklasifikasi.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Message", JOptionPane.INFORMATION_MESSAGE);
        }else{
        String sql = "insert into klasifikasi values (?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kode_klasifikasi.getText());
            stat.setString(2, klasifikasi.getText());
            stat.setString(3, subkualifikasi.getText());
            stat.setString(4, kode_subklasifikasi.getText());
            stat.setString(5, subklasifikasi.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            kode_klasifikasi.requestFocus();
            datatable();

        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal disimpan "+e);
        } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

