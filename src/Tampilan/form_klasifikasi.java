    private void tcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyReleased
    tabmode.getDataVector().removeAllElements();
    tabmode.fireTableDataChanged();
        try {
            String sql = "select * from klasifikasi where kd_klasifikasi like '%" + tcari.getText() + "%' or klasifikasi_bu like'%" + tcari.getText() + "%' or subkualifikasi like'%" + tcari.getText() + "%' or kd_subklasifikasi like'%" + tcari.getText() + "%' or subklasifikasi like'%" + tcari.getText() + "%'";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                    String a = hasil.getString("kd_klasifikasi");
                    String b = hasil.getString("klasifikasi_bu");
                    String c = hasil.getString("subkualifikasi");
                    String d = hasil.getString("kd_subklasifikasi");
                    String e = hasil.getString("subklasifikasi");
            String[] data = {a,b,c,d,e};
            tabmode.addRow(data);
        }
         hasil.close();
            stat.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }//GEN-LAST:event_tcariKeyReleased

