    protected void datatable(){
        Object[] Baris ={"Kode Klasifikasi ","Klasifikai Bidang Usaha","Subkualifikasi","Kode Subklasifikasi","Subklasifikasi"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelklasifikasi.setModel(tabmode);
        String cariitem =tcari.getText();
        try{
            String sql="select *from klasifikasi where kd_klasifikasi like'%"+cariitem+"%'or klasifikasi_bu like"+"'%"+cariitem+"%'order by kd_subklasifikasi asc";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
            String a = hasil.getString("kd_klasifikasi");
            String b = hasil.getString("klasifikasi_bu");
            String c = hasil.getString("subkualifikasi");
            String d = hasil.getString("kd_subklasifikasi");
            String e = hasil.getString("subklasifikasi");
            String[] data = {a,b,c,d,e};
            tabmode.addRow(data);
        }
        }catch (SQLException e){
                JOptionPane.showMessageDialog(null,"Gagal dipanggil"+e);
            }
        } 


