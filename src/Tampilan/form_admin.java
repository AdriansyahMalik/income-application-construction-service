    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String jenis = null;
        if(laki.isSelected()){
            jenis = laki.getText();
        } else if(perempuan.isSelected()){
            jenis = perempuan.getText();
        }
        String sql = "insert into tabel_admin values(?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,id_admin.getText());
            stat.setString(3,nama_admin.getText());
            stat.setString(4,jenis);
            stat.setString(2,password.getText());
            stat.setString(5,alamat.getText());
            stat.setString(6,no_hp.getText());
            stat.setString(7,email.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil disimpan");
            kosong();
            nama_admin.requestFocus();
            datatable();
            autonumber();
            
            
            
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
        }
    }//GEN-LAST:event_saveActionPerformed

