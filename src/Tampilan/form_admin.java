    private void tcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyReleased
        tabmode.getDataVector().removeAllElements();
        tabmode.fireTableDataChanged();
        try {
            String sql = "select * from tabel_admin where id_admin like '%" + tcari.getText() + "%' or nama_user like'%" + tcari.getText() + "%' or alamat like'%" + tcari.getText() + "%'  ";
            try (java.sql.Statement stat = conn.createStatement()) {
                ResultSet hasil = stat.executeQuery(sql);
                while(hasil.next()){
                    String a = hasil.getString("id_admin");
                    String b = hasil.getString("nama_user");
                    String c = hasil.getString("jk");
                    String d = hasil.getString("alamat");
                    String e = hasil.getString("no_hp");
                    String f = hasil.getString("email");

            String[] data = {a,b,c,d,e,f};
            tabmode.addRow(data);
                }
                hasil.close();
                    stat.close();
            }
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
                                

    }//GEN-LAST:event_tcariKeyReleased

