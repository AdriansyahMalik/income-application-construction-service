    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        String sql = "SELECT * FROM tabel_admin WHERE id_admin='"+user.getText()+"' AND password='"+pass.getText()+"'";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        if(hasil.next()){
            UserID.setUserLogin(hasil.getString("id_admin"));
            UserID.setNamaLogin(hasil.getString("nama_user"));
            JOptionPane.showMessageDialog(null,"Berhasil Login");
            
            this.setVisible(false);
            new menu_utama().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null,"ID atau Password Salah");    
        }
    }
    catch (SQLException e){
        JOptionPane.showMessageDialog(null,"Gagal Login !!!" +e); 
    }
    }//GEN-LAST:event_jButton1ActionPerformed

